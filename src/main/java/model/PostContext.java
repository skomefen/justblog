package model;

public class PostContext {
	
	private String contextAdress;
	private byte[] context;
	
	public String getContextAdress() {
		return contextAdress;
	}
	public void setContextAdress(String contextAdress) {
		this.contextAdress = contextAdress;
	}
	public byte[] getContext() {
		return context;
	}
	public void setContext(byte[] context) {
		this.context = context;
	}
	
}
